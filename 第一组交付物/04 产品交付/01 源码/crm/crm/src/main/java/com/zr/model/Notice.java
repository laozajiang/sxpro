package com.zr.model;

public class Notice {
		private String notice_name;
		private String notice_info;
		private String notice_user;
		public String getname() {
			return notice_name;
		}
		public void setname(String notice_name) {
			this.notice_name = notice_name;
		}
		public String getinfo() {
			return notice_info;
		}
		public void setinfo(String notice_info) {
			this.notice_info = notice_info;
		}
		public String getuser() {
			return notice_user;
		}
		public void setuser(String notice_user) {
			this.notice_user = notice_user;
		}

	
		
		
		
}
